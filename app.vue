<template>
    <div :class="{ 'dark': darkMode }">
        <div class="bg-white dark:bg-dim-900">

            <LoadingPage v-if="isAuthLoading" />

            <!-- App -->
            <div v-else-if="user" clas="min-h-full">

                <div class="grid grid-cols-12 mx-auto sm:px-6 lg:max-w-7xl lg:px-8 lg:gap-5">

                    <!-- Left sidebar -->
                    <div class="hidden md:block xs-col-span-1 xl:col-span-2">
                        <div class="sticky top-0">
                            <SidebarLeft :user="user"  @on-tweet="handleOpenTweetModal" @on-logout="handleUserLogout" />
                        </div>
                    </div>

                    <!-- Main content -->
                    <main class="col-span-12 md:col-span-8 xl:col-span-6">
                        <router-view />
                    </main>

                    <!-- Right Sidebar -->
                    <div class="hidden col-span-12 md:block xl:col-span-4 md:col-span-3">
                        <div class="sticky top-0">
                            <SidebarRight />
                        </div>
                    </div>


                </div>


            </div>

            <AuthPage v-else />


            <UIModal :isOpen="postTweetModal" @on-close="handleModalClose">
                <TweetForm :replyTo="replyTweet" showReply :user="user" @onSuccess="handleFormSuccess" />
            </UIModal>

        </div>

    </div>
</template>
<script setup>
import { ref, onBeforeMount } from 'vue';

// --- Dark Mode ---
const darkMode = ref(false);

// --- Auth ---
// Yeni useAuth yapısından ihtiyacımız olan her şeyi tek seferde alıyoruz
// "loading" state'ini bu dosyada "isAuthLoading" olarak yeniden isimlendiriyoruz
const { user, initAuth, loading: isAuthLoading, logout } = useAuth();

// --- Tweet Modal ---
const { closePostTweetModal, usePostTweetModal, openPostTweetModal, useReplyTweet } = useTweets();
const postTweetModal = usePostTweetModal();
const replyTweet = useReplyTweet();

// --- Event Emitter ---
const emitter = useEmitter();
emitter.$on('replyTweet', (tweet) => openPostTweetModal(tweet));
emitter.$on('toggleDarkMode', () => darkMode.value = !darkMode.value);

// --- Lifecycle Hooks ---
// Bileşen yüklenmeden önce kimlik doğrulama kontrolünü başlat
onBeforeMount(() => {
    initAuth();
});

// --- Functions ---
function handleFormSuccess(tweet) {
    closePostTweetModal();
    navigateTo({ path: `/status/${tweet.id}` });
}

function handleModalClose() {
    closePostTweetModal();
}

function handleOpenTweetModal() {
    openPostTweetModal(null);
}

function handleUserLogout() {
    logout();
}
</script>
